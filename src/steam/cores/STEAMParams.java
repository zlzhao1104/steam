package steam.cores;

public class STEAMParams {
    
    private int width;
    private int height;
    private String shpDir;
    
    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public String getShpDir() {
        return shpDir;
    }

    public void setShpDir(String shpDir) {
        this.shpDir = shpDir;
    }
    
    public STEAMParams() {
	
    }

}
